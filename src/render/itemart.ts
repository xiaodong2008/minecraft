// String art: '.' transparent, letters looked up in the palette.
function art(rows: string[], palette: Record<string, RGB>): (ctx: CanvasRenderingContext2D) => void {
  return (ctx) => {
    for (let y = 0; y < rows.length; y++) {
      for (let x = 0; x < rows[y].length; x++) {
        const ch = rows[y][x];
        if (ch === '.' || ch === ' ') continue;
        const c = palette[ch];
        if (!c) continue;
        ctx.fillStyle = `rgb(${c[0]},${c[1]},${c[2]})`;
        ctx.fillRect(x, y, 1, 1);
      }
    }
  };
}

